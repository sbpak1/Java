package ch02;

public class Q3 {
//    1부터 20까지의 수를 출력하세요. (단, 2의 배수일 때만 출력하세요)
//    for 문법 사용
    public static void main(String[] args) {
        // 루프 생성
        for (int i = 1; i <= 20; i++) {
            // 조건 생성 (2의 배수)
            if (i % 2 == 0) {
                System.out.println(i);
            }

            }


        }
    }

